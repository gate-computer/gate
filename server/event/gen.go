package main

import (
	"bytes"
	"fmt"
	"io/ioutil"
	"os"
	"sort"
	"strings"
	"unicode"

	"gate.computer/gate/server/event/pb"
	m "import.name/make"
)

func main() {
	var (
		gofmt  = os.Args[1]
		output = os.Args[2]
	)

	b := bytes.NewBuffer(nil)

	fmt.Fprintln(b, "// Code generated by gen.go, DO NOT EDIT!")
	fmt.Fprintln(b)
	fmt.Fprintln(b, "package event")
	fmt.Fprintln(b)
	fmt.Fprintln(b, `import "gate.computer/gate/server/event/pb"`)
	fmt.Fprintln(b)

	names := []string{}
	for _, name := range pb.Type_name {
		names = append(names, name)
	}
	sort.Strings(names)

	fmt.Fprintln(b, "const (")
	for _, name := range names {
		fmt.Fprintf(b, "Type%s = pb.Type_%s\n", snake2title(name), name)
	}
	fmt.Fprintln(b, ")")
	fmt.Fprintln(b)

	names = []string{}
	for _, name := range pb.Fail_Type_name {
		names = append(names, name)
	}
	sort.Strings(names)

	fmt.Fprintln(b, "const (")
	for _, name := range names {
		fmt.Fprintf(b, "Fail%s = pb.Fail_%s\n", snake2title(name), name)
	}
	fmt.Fprintln(b, ")")
	fmt.Fprintln(b)

	data, err := m.RunIO(b, gofmt)
	check(err)

	check(ioutil.WriteFile(output, data, 0o666))
}

func snake2title(snake string) string {
	var (
		title string
		skip  int
		up    = true
	)

	for i, code := range snake {
		if skip > 0 {
			skip--
			continue
		}

		if code == '_' {
			switch tail := snake[i:]; {
			case tail == "_ID":
				return title + "ID"
			case strings.HasPrefix(tail, "_ID_"):
				title += "ID"
				skip = 3
			}
			up = true
		} else {
			r := rune(code)
			if up {
				r = unicode.ToUpper(r)
				up = false
			} else {
				r = unicode.ToLower(r)
			}
			title += string(r)
		}
	}

	return title
}

func check(err error) {
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}
